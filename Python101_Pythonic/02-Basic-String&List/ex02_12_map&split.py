def map_split05():
    input_sensor = 'Sensor-T1: 25 30 22 40 19'
    sensor_id, temp = input_sensor.strip().split(': ')
    print(sensor_id)

    # ‡πÉ‡∏ä‡πâ function map ‡∏à‡∏≥‡∏Ñ‡∏π‡πà int ‡πÅ‡∏õ‡∏•‡∏á‡∏Ñ‡πà‡∏≤
