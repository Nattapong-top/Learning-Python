'''
üéØ ‡πÇ‡∏à‡∏ó‡∏¢‡πå‡∏ó‡∏µ‡πà 4: ‡∏ô‡∏±‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏Ç‡πâ‡∏≠‡∏ó‡∏µ‡πà‡∏ï‡∏≠‡∏ö‡∏ú‡∏¥‡∏î
‡∏™‡∏ñ‡∏≤‡∏ô‡∏Å‡∏≤‡∏£‡∏ì‡πå: ‡∏Ñ‡∏∏‡∏ì‡∏°‡∏µ "‡πÄ‡∏â‡∏•‡∏¢" ‡πÅ‡∏•‡∏∞ "‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö‡∏Ç‡∏≠‡∏á‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô" 
‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡∏ô‡∏±‡∏ö‡∏ß‡πà‡∏≤‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô‡∏ï‡∏≠‡∏ö‡∏ú‡∏¥‡∏î‡∏Å‡∏µ‡πà‡∏Ç‡πâ‡∏≠ (‡πÄ‡∏õ‡∏£‡∏µ‡∏¢‡∏ö‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡∏ó‡∏µ‡∏•‡∏∞‡∏Ñ‡∏π‡πà)
'''

def zip04():
    answer_key = ['A', 'B', 'A', 'D', 'C']
    student_answers = ['A', 'C', 'A', 'D', 'B']

    wrong_count =  sum(1 for key, ans in zip(answer_key, student_answers) if key != ans)

    print(wrong_count)
zip04()

'''
üéØ ‡πÇ‡∏à‡∏ó‡∏¢‡πå‡∏ó‡∏µ‡πà 5: ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏ñ‡πà‡∏ß‡∏á‡∏ô‡πâ‡∏≥‡∏´‡∏ô‡∏±‡∏Å (Weighted Sum)
‡∏™‡∏ñ‡∏≤‡∏ô‡∏Å‡∏≤‡∏£‡∏ì‡πå: ‡∏Ñ‡∏∏‡∏ì‡∏°‡∏µ "‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô" ‡πÅ‡∏•‡∏∞ "‡∏Ñ‡πà‡∏≤‡∏ô‡πâ‡∏≥‡∏´‡∏ô‡∏±‡∏Å" (Weight) ‡∏Ç‡∏≠‡∏á‡πÅ‡∏ï‡πà‡∏•‡∏∞‡∏™‡πà‡∏ß‡∏ô ‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏£‡∏ß‡∏°‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢ (‡∏Ñ‡∏•‡πâ‡∏≤‡∏¢ Dot Product ‡πÅ‡∏ï‡πà‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Å‡∏≤‡∏£‡∏Ñ‡∏¥‡∏î‡πÄ‡∏Å‡∏£‡∏î)
'''
def zip05():
    scores = [80, 90, 75]       # ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏™‡∏≠‡∏ö‡∏¢‡πà‡∏≠‡∏¢, ‡∏Å‡∏•‡∏≤‡∏á‡∏†‡∏≤‡∏Ñ, ‡∏õ‡∏•‡∏≤‡∏¢‡∏†‡∏≤‡∏Ñ
    weights = [0.2, 0.3, 0.5]  # ‡∏ô‡πâ‡∏≥‡∏´‡∏ô‡∏±‡∏Å 20%, 30%, 50%

    final_score = sum((s*w) for s, w in zip(scores, weights))

    print(final_score)
zip05()

'''
üéØ ‡πÇ‡∏à‡∏ó‡∏¢‡πå‡∏ó‡∏µ‡πà 6: zip ‡∏Å‡∏±‡∏ö 3 ‡∏•‡∏¥‡∏™‡∏ï‡πå
‡∏™‡∏ñ‡∏≤‡∏ô‡∏Å‡∏≤‡∏£‡∏ì‡πå: ‡∏Ñ‡∏∏‡∏ì‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• 3 ‡∏ä‡∏∏‡∏î‡∏ó‡∏µ‡πà‡∏™‡∏≠‡∏î‡∏Ñ‡∏•‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô ‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡∏£‡∏ß‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡πâ‡∏á 3 ‡∏ä‡∏∏‡∏î ‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡∏•‡∏¥‡∏™‡∏ï‡πå‡∏Ç‡∏≠‡∏á Tuples
'''

def zip06():
    products = ['Apple', 'Banana', 'Cherry']
    prices = [30, 10, 50]
    quantities = [5, 10, 3]

    inventory = list(zip(products, prices, quantities))

    print(inventory)
