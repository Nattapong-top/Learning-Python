def zip09():
    limits = [100, 50, 200]
    # values = [90, 45, 190]
    values = [90, 55, 190]

    # ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô all() ‡∏à‡∏∞‡∏Ñ‡∏∑‡∏ô‡∏Ñ‡πà‡∏≤ True ‡∏ñ‡πâ‡∏≤‡∏ó‡∏∏‡∏Å‡∏Ñ‡πà‡∏≤‡πÉ‡∏ô generator ‡πÄ‡∏õ‡πá‡∏ô True
    all_passed = all( v < l for l, v in zip(limits, values))

    print(f'‡∏ú‡πà‡∏≤‡∏ô‡∏Å‡∏≤‡∏£‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î: {all_passed}')
zip09()

