'''การ "จำลองการ Input" โดยไม่ต้องพิมพ์เอง มี 2 แบบที่เหมาะกับป๋าครับ'''
'''แบบที่ 1: ใช้ for loop วนอ่านจากลิสต์ (วิธีมาตรฐาน)
วิธีนี้ง่ายที่สุดครับ คือเราเตรียมข้อมูลใส่ List ไว้ก่อน แล้วใช้ 
for วนเอาข้อมูลออกมาทีละตัว แทนที่จะใช้คำสั่ง input() ครับ'''

import time

# 1. เตรียมข้อมูลใส่ถังไว้ก่อน (เหมือนเตรียมสคริปต์)
# สมมติเป็นรายชื่อพนักงานที่ป๋าอยากกรอก
data_list = ["สมชาย", "สมหญิง", "บัวขาว", "แจ็คสัน"]

print("--- เริ่มจำลองการกรอกข้อมูล ---")

# 2. ใช้ for loop ดึงออกมาทีละคน
for name in data_list:
    # ตรงนี้ name จะทำหน้าที่เหมือนสิ่งที่เรารับมาจาก input()
    print(f"ระบบ: กรุณากรอกชื่อพนักงาน...")
    
    time.sleep(1) # แกล้งหน่วงเวลาหน่อย ให้เหมือนคนกำลังพิมพ์
    print(f"ผู้ใช้ (จำลอง): {name}") # แสดงค่าที่ดึงมา
    
    # ... เอาตัวแปร name ไปคำนวณหรือทำอะไรต่อ ...
    print(f">> บันทึกข้อมูลคุณ {name} เรียบร้อย!\n")

print("--- จบการทำงาน ---")



