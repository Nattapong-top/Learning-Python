'''แบบที่ 2: สร้างฟังก์ชัน "Input ปลอม" (Advance ขึ้นนิดนึง)
ถ้าป๋าอยากเขียนโค้ดให้หน้าตาเหมือนใช้ input() ปกติ แต่ไม่อยากพิมพ์เอง
เราสามารถสร้างฟังก์ชันมาหลอกโปรแกรมได้ครับ 
โดยใช้คำสั่ง .pop(0) (ดึงตัวแรกออกแล้วลบทิ้ง)
วิธีนี้เท่ตรงที่ มันจะดึงข้อมูลออกมาทีละตัวตามลำดับ เหมือนเราคีย์เข้าไปจริงๆ'''
import time

# 1. เตรียมข้อมูลรอไว้
mock_inputs = ["ณัฐพงศ์", "50000", "DevOps"]

# 2. สร้างฟังก์ชัน Input ปลอมๆ ขึ้นมา
def fake_input(prompt_text):
    print(prompt_text, end="") # ปริ้นท์คำถามออกมา
    time.sleep(2) # แกล้งคิดแป๊บนึง
    
    if mock_inputs: # ถ้ายังมีข้อมูลในลิสต์
        value = mock_inputs.pop(0) # ดึงตัวแรกสุดออกมา แล้วลบออกจากลิสต์
        print(value) # ปริ้นท์ค่าออกมาให้เห็นเหมือนเราพิมพ์
        return value
    else:
        return "" # ถ้าข้อมูลหมดแล้ว ให้คืนค่าว่าง

# --- เริ่มโปรแกรมหลัก ---
print("--- โปรแกรมคำนวณภาษี (โหมดจำลอง) ---")

# สังเกตว่าเราใช้ fake_input แทน input ปกติ
name = fake_input("กรุณากรอกชื่อ: ")
salary = fake_input("กรุณากรอกเงินเดือน: ")
job = fake_input("ตำแหน่งงาน: ")

print("----------------")
print(f"สรุป: คุณ{name} ทำงาน {job} เงินเดือน {salary} บาท")