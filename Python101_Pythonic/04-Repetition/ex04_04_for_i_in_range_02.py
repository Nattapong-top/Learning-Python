'''
‡∏Ç‡πâ‡∏≠‡∏ó‡∏µ‡πà 5: ‡πÅ‡∏ü‡∏Å‡∏ó‡∏≠‡πÄ‡∏£‡∏µ‡∏¢‡∏•‡∏ó‡∏≥‡∏°‡∏∑‡∏≠ (Factorial)
Input: ‡∏£‡∏±‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡πÄ‡∏ï‡πá‡∏°‡∏ö‡∏ß‡∏Å 
Task: ‡∏à‡∏á‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡πà‡∏≤ n! (n ‡πÅ‡∏ü‡∏Å‡∏ó‡∏≠‡πÄ‡∏£‡∏µ‡∏¢‡∏•) ‡∏ã‡∏∂‡πà‡∏á‡∏Ñ‡∏∑‡∏≠‡∏ú‡∏•‡∏Ñ‡∏π‡∏ì‡∏à‡∏≤‡∏Å 1 ‡∏ñ‡∏∂‡∏á n 
‡∏™‡∏π‡∏ï‡∏£: n! = 1 x 2 x 3 x ... x n
‡πÄ‡∏ä‡πà‡∏ô ‡∏£‡∏±‡∏ö 5 -> ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì 1 x 2 x 3 x 4 x 5 = 120
Output: ‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏Å‡∏≤‡∏£‡∏Ñ‡∏π‡∏ì
'''
def for_factorial():
    n = 5 # int(input(num n: ))
    total_sum = 1

    for i in range(1,n+1):
        total_sum = total_sum * i
        print(f'i = {i}, total sum = {total_sum}')

# for_factorial()

