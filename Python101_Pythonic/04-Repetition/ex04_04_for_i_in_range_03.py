'''
‡∏Ç‡πâ‡∏≠‡∏ó‡∏µ‡πà 7: ‡∏ß‡∏¥‡πà‡∏á‡∏¢‡∏≤‡∏ß‡∏ï‡πà‡∏≠‡πÄ‡∏ô‡∏∑‡πà‡∏≠‡∏á (Longest Run)
Input: ‡∏£‡∏±‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° s ‡∏°‡∏≤‡∏´‡∏ô‡∏∂‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°
Task: ‡∏´‡∏≤‡∏ß‡πà‡∏≤‡∏ï‡∏±‡∏ß‡∏≠‡∏±‡∏Å‡∏©‡∏£‡πÉ‡∏î "‡∏ß‡∏¥‡πà‡∏á" (‡∏≠‡∏¢‡∏π‡πà‡∏ï‡∏¥‡∏î‡∏Å‡∏±‡∏ô) ‡πÑ‡∏î‡πâ‡∏¢‡∏≤‡∏ß‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î ‡πÅ‡∏•‡∏∞‡∏ß‡∏¥‡πà‡∏á‡πÑ‡∏î‡πâ‡∏Å‡∏µ‡πà‡∏ï‡∏±‡∏ß
Output: (‡∏ï‡∏±‡∏ß‡∏≠‡∏¢‡πà‡∏≤‡∏á s = 'AABBBCCCCDAA')
'''

def for_longest_run():
    s = 'AABBBCCCCDAA'

    # 1. ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô ‡πÇ‡∏î‡∏¢‡∏≠‡∏¥‡∏á‡∏à‡∏≤‡∏Å "‡∏ï‡∏±‡∏ß‡πÅ‡∏£‡∏Å" (index 0)
    max_char = s[0]     # ‡πÄ‡∏Å‡πá‡∏ö‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥ ‡∏ï‡∏±‡∏ß‡∏≠‡∏±‡∏Å‡∏©‡∏£ ‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î
    max_count = 1       # ‡πÄ‡∏Å‡πá‡∏ö‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥ ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô ‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î

    current_char = s[0] # ‡∏ï‡∏±‡∏ß‡∏ó‡∏µ‡πà‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏ô‡∏±‡∏ö
    current_count = 1   # ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡∏ô‡∏±‡∏ö‡πÑ‡∏î‡πâ‡∏ï‡∏≠‡∏ô‡∏ô‡∏µ‡πâ

    # 2. ‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡∏ï‡∏±‡πâ‡∏á‡πÅ‡∏ï‡πà "‡∏ï‡∏±‡∏ß‡∏ó‡∏µ‡πà‡∏™‡∏≠‡∏á" (index 1) ‡πÑ‡∏õ‡∏à‡∏ô‡∏à‡∏ö
    # ‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ‡∏ß‡∏¥‡∏ò‡∏µ ‡πÄ‡∏ó‡∏µ‡πà‡∏¢‡∏ö‡∏Å‡∏±‡∏ö‡∏ï‡∏±‡∏ß‡∏Å‡πà‡∏≠‡∏ô‡∏´‡∏ô‡πâ‡∏≤ 
    for i in range(1, len(s)):

        # 3. ‡πÄ‡∏ä‡πá‡∏Å‡∏ß‡πà‡∏≤‡∏ï‡∏±‡∏ß‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô (s[i]) ‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡∏ï‡∏±‡∏ß‡∏Å‡πà‡∏≠‡∏ô‡∏´‡∏ô‡πâ‡∏≤ (s[i-1]) ‡πÑ‡∏´‡∏°
        if s[i] == s[i-1]:

            # ‡∏ñ‡πâ‡∏≤‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô: ‡πÉ‡∏´‡πâ‡∏ô‡∏±‡∏ö‡∏ï‡πà‡∏≠
            current_count += 1
        
        else:
            # ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô: ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ô‡∏±‡∏ö‡∏ï‡∏±‡∏ß‡πÉ‡∏´‡∏°‡πà
            current_char = s[i]
            current_count = 1
        
        # 4. ‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å‡∏ô‡∏±‡∏ö‡πÄ‡∏™‡∏£‡πá‡∏à ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏ó‡∏≥‡∏•‡∏≤‡∏¢‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
        if current_count > max_count:
            max_count = current_count
            max_char = s[i]

    print(f'{max_char} ‡∏ß‡∏¥‡πà‡∏á‡πÑ‡∏î‡πâ {max_count}')
for_longest_run()

'''
‡∏Ç‡πâ‡∏≠‡∏ó‡∏µ‡πà 8: ‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ (Proper Divisors)

Input: ‡∏£‡∏±‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡πÄ‡∏ï‡πá‡∏°‡∏ö‡∏ß‡∏Å n

Task: ‡∏´‡∏≤‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏Ç‡∏≠‡∏á "‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ" (Proper Divisors) ‡∏Ç‡∏≠‡∏á n. (‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ‡∏Ñ‡∏∑‡∏≠‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà‡∏´‡∏≤‡∏£ n ‡∏•‡∏á‡∏ï‡∏±‡∏ß ‡πÇ‡∏î‡∏¢‡πÑ‡∏°‡πà‡∏ô‡∏±‡∏ö‡∏ï‡∏±‡∏ß n ‡πÄ‡∏≠‡∏á)

‡πÄ‡∏ä‡πà‡∏ô n = 6 ‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ‡∏Ñ‡∏∑‡∏≠ 1, 2, 3

‡πÄ‡∏ä‡πà‡∏ô n = 10 ‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ‡∏Ñ‡∏∑‡∏≠ 1, 2, 5

Output: (‡∏ï‡∏±‡∏ß‡∏≠‡∏¢‡πà‡∏≤‡∏á n = 6)
'''

def for_peoper_divisors():
    n = 6   #int(input('num: '))

    # 1. ‡∏ï‡∏±‡πâ‡∏á‡∏ï‡∏±‡∏ß‡πÅ‡∏õ‡∏£‡πÄ‡∏Å‡πá‡∏ö‡∏ú‡∏•‡∏£‡∏ß‡∏°
    total_sum = 0

    # 2. ‡∏ß‡∏ô‡∏•‡∏π‡∏õ i ‡∏ï‡∏±‡πâ‡∏á‡πÅ‡∏ï‡πà 1 ‡∏ñ‡∏∂‡∏á n-1
    # (range(1, n) ‡∏à‡∏∞‡∏ß‡∏¥‡πà‡∏á‡∏ñ‡∏∂‡∏á n-1 ‡∏û‡∏≠‡∏î‡∏µ)
    for i in range(1, n+1):

        # 3. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤ n ‡∏´‡∏≤‡∏£ i ‡∏•‡∏á‡∏ï‡∏±‡∏ß‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
        if n % i == 0:
            # 4. ‡∏ñ‡πâ‡∏≤ i ‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ ‡πÉ‡∏´‡πâ‡∏ö‡∏ß‡∏Å‡πÄ‡∏Ç‡πâ‡∏≤‡πÑ‡∏õ‡πÉ‡∏ô‡∏ú‡∏•‡∏£‡∏ß‡∏°
            total_sum = i

    print(f"‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏ï‡∏±‡∏ß‡∏´‡∏≤‡∏£‡πÅ‡∏ó‡πâ‡∏Ç‡∏≠‡∏á {n} ‡∏Ñ‡∏∑‡∏≠: {total_sum}")
for_peoper_divisors()

'''
9. ‡∏Ñ‡∏π‡πà‡∏ó‡∏µ‡πà‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î
‡∏Ñ‡∏≥‡πÉ‡∏ö‡πâ: ‡∏°‡∏µ 2 ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏Ñ‡∏£‡∏±‡∏ö

‡∏Ç‡∏±‡πâ‡∏ô‡∏ó‡∏µ‡πà 1: ‡πÅ‡∏õ‡∏•‡∏á Input str (‡πÄ‡∏ä‡πà‡∏ô "1 5 9 3") ‡πÉ‡∏´‡πâ‡∏Å‡∏•‡∏≤‡∏¢‡πÄ‡∏õ‡πá‡∏ô list ‡∏Ç‡∏≠‡∏á‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç int ‡∏Å‡πà‡∏≠‡∏ô (‡∏≠‡∏≤‡∏à‡∏à‡∏∞‡∏ï‡πâ‡∏≠‡∏á‡πÉ‡∏ä‡πâ .split() ‡πÅ‡∏•‡πâ‡∏ß‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡πÅ‡∏õ‡∏•‡∏á int)

‡∏Ç‡∏±‡πâ‡∏ô‡∏ó‡∏µ‡πà 2: ‡∏û‡∏≠‡πÑ‡∏î‡πâ list ‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡πÅ‡∏•‡πâ‡∏ß ‡∏Å‡πá‡πÉ‡∏ä‡πâ for i in range(len(list)-1) (‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡∏Ç‡πâ‡∏≠ 3 ‡∏°‡∏ô‡∏∏‡∏©‡∏¢‡πå‡∏Ñ‡∏π‡πà‡∏Ç‡∏ô‡∏≤‡∏ô) ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏ä‡πá‡∏Å list[i] + list[i+1] ‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡∏Å‡∏±‡∏ö '‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î' (max_sum) ‡∏ó‡∏µ‡πà‡∏õ‡πã‡∏≤‡πÄ‡∏Ñ‡∏¢‡πÄ‡∏à‡∏≠‡∏Ñ‡∏£‡∏±‡∏ö
'''
def sum_list():
    digit = '1 5 9 3 4 6 2'

    # --- ‡∏Ç‡∏±‡πâ‡∏ô‡∏ó‡∏µ‡πà 1: ‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ---
    # ‡πÅ‡∏¢‡∏Å str ‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô list ‡πÅ‡∏•‡∏∞ ‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏õ‡πá‡∏ô int
    nums_int = list(map(int, digit.split(' ')))

    # --- ‡∏Ç‡∏±‡πâ‡∏ô‡∏ó‡∏µ‡πà 2: ‡∏´‡∏≤‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î ---
    # 1. ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô (‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥) ‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ "‡∏Ñ‡∏π‡πà‡πÅ‡∏£‡∏Å" ‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏±‡πâ‡∏á
    max_sum = nums_int[0] + nums_int[1]
    max_pair = (nums_int[0], nums_int[1])

    # 2. ‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏ä‡πá‡∏Å‡∏ó‡∏µ‡∏•‡∏∞‡∏Ñ‡∏π‡πà (‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡∏Ç‡πâ‡∏≠ 3 ‡∏ï‡πâ‡∏≠‡∏á -1)
    for i in range(len(nums_int) -1):
        
        # 6. ‡∏´‡∏≤‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏π‡πà‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô
        current_sum = nums_int[i] +  nums_int[i+1]
        
        # 7. ‡πÄ‡∏ä‡πá‡∏Å‡∏ß‡πà‡∏≤‡∏ó‡∏≥‡∏•‡∏≤‡∏¢‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
        if current_sum > max_sum:
            max_sum = current_sum
            max_pair = (nums_int[i], nums_int[i+1])
    
    print(f'‡∏Ñ‡∏π‡πà‡∏ó‡∏µ‡πà‡∏£‡πà‡∏ß‡∏°‡∏Å‡∏±‡∏ô‡πÑ‡∏î‡πâ‡∏°‡∏≤‡∏Å‡∏™‡∏∏‡∏î‡∏Ñ‡∏∑‡∏≠ {max_pair} ‡∏£‡∏ß‡∏°‡πÑ‡∏î‡πâ {max_sum}')

sum_list()

'''
‡∏Ç‡πâ‡∏≠‡∏ó‡∏µ‡πà 10: ‡∏û‡∏≤‡∏•‡∏¥‡∏ô‡πÇ‡∏î‡∏£‡∏° (Palindrome Checker)
Input: ‡∏£‡∏±‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° s
Task: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤ s (‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÑ‡∏°‡πà‡∏™‡∏ô‡∏ï‡∏±‡∏ß‡∏û‡∏¥‡∏°‡∏û‡πå‡πÄ‡∏•‡πá‡∏Å/‡πÉ‡∏´‡∏ç‡πà) ‡∏≠‡πà‡∏≤‡∏ô‡∏à‡∏≤‡∏Å‡∏´‡∏ô‡πâ‡∏≤‡πÑ‡∏õ‡∏´‡∏•‡∏±‡∏á ‡πÅ‡∏•‡∏∞ ‡∏´‡∏•‡∏±‡∏á‡πÑ‡∏õ‡∏´‡∏ô‡πâ‡∏≤ ‡πÑ‡∏î‡πâ‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡∏Å‡∏±‡∏ô‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
Output:
madam -> True
Natta -> False
Racecar -> True
'''
def palindrome(s:str):
    
    # 1. ‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡∏û‡∏¥‡∏°‡∏û‡πå‡πÄ‡∏•‡πá‡∏Å‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
    s_clean = s.lower()
    s_clean = s.isalpha

    # 2. ‡∏™‡∏£‡πâ‡∏≤‡∏á string ‡∏ó‡∏µ‡πà‡∏Å‡∏•‡∏±‡∏ö‡∏î‡πâ‡∏≤‡∏ô ‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ Slicing
    # [::-1] ‡∏´‡∏°‡∏≤‡∏¢‡∏ñ‡∏∂‡∏á "‡πÄ‡∏£‡∏¥‡πà‡∏°‡πÅ‡∏ï‡πà‡∏ï‡πâ‡∏ô‡∏à‡∏ô‡∏à‡∏ö ‡πÅ‡∏ï‡πà‡∏Å‡πâ‡∏≤‡∏ß‡∏ó‡∏µ‡∏•‡∏∞ -1 (‡∏ñ‡∏≠‡∏¢‡∏´‡∏•‡∏±‡∏á)"
    s_reversed = s_clean[::-1]
    
    # 3. ‡πÄ‡∏õ‡∏£‡∏µ‡∏¢‡∏ö‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö
    is_palindrome = (s_clean == s_reversed)

    # 4. ‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•
    print(f'‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°: {s} ‡πÄ‡∏õ‡πá‡∏ô palindrome: {is_palindrome}')

palindrome('Racecar')
palindrome('madam')
palindrome('Natta')

