'''‡∏Ç‡πâ‡∏≠ 3: ‡∏ú‡∏•‡∏£‡∏ß‡∏°‡πÄ‡∏•‡∏Ç‡∏Ñ‡∏π‡πà (Sum of Evens)

‡∏£‡∏±‡∏ö‡∏Ñ‡πà‡∏≤ n ‡∏à‡∏≤‡∏Å input

‡∏à‡∏á‡∏´‡∏≤‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏Ç‡∏≠‡∏á ‡πÄ‡∏•‡∏Ç‡∏Ñ‡∏π‡πà ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î ‡∏ï‡∏±‡πâ‡∏á‡πÅ‡∏ï‡πà 0 ‡∏ñ‡∏∂‡∏á n (‡∏£‡∏ß‡∏° n ‡∏î‡πâ‡∏ß‡∏¢‡∏ñ‡πâ‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏•‡∏Ç‡∏Ñ‡∏π‡πà)

‡∏Ñ‡∏≥‡πÉ‡∏ö‡πâ: ‡πÉ‡∏ä‡πâ range(0, n+1, 2) ‡∏à‡∏∞‡∏ä‡πà‡∏ß‡∏¢‡πÉ‡∏´‡πâ‡∏ä‡∏µ‡∏ß‡∏¥‡∏ï‡∏á‡πà‡∏≤‡∏¢‡∏Ç‡∏∂‡πâ‡∏ô‡∏°‡∏≤‡∏Å‡∏Ñ‡∏£‡∏±‡∏ö!
'''

def for_sum_of_evens(n:int):
    total = 0
    for i in range(0, n+1, 2):
        total += i
    return f'‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏Ç‡∏≠‡∏á ‡πÄ‡∏•‡∏Ç‡∏Ñ‡∏π‡πà 0 ‡∏ñ‡∏∂‡∏á {n}: {total}'

print(f'{for_sum_of_evens(4)}')

