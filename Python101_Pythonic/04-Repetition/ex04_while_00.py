def count_nums_list():
    nums = [10, -5, 0, 20, -1, 3]
    i = 0
    count = 0
    nums_deteils = []

    while i < len(nums):
        if nums[i] > 0:
            count += 1

            if nums[i] not in nums_deteils:
                nums_deteils.append(nums[i])
        
        i += 1
    
    print(f"Num: {nums_deteils}, Count: {count}")
count_nums_list()

