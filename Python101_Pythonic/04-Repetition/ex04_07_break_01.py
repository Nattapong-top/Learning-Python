'''4. ü•á ‡πÇ‡∏à‡∏ó‡∏¢‡πå: ‡∏´‡∏≤‡∏ú‡∏π‡πâ‡∏ä‡∏ô‡∏∞ (Boolean Flag + break)
‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•: players = ["Tom", "Spike", "Admin", "Jerry"]
‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ "Admin" ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÄ‡∏•‡πà‡∏ô‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
‡∏ï‡∏£‡∏£‡∏Å‡∏∞:‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏±‡∏ß‡πÅ‡∏õ‡∏£ "‡∏ò‡∏á" (Flag) -> found_admin = False
‡πÉ‡∏ä‡πâ‡∏ß‡∏á‡∏ß‡∏ô for ‡∏ß‡∏ô‡∏ã‡πâ‡∏≥‡πÉ‡∏ô players ‡∏ñ‡πâ‡∏≤ ‡πÄ‡∏à‡∏≠ "Admin" 
-> ‡πÉ‡∏´‡πâ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ found_admin = True ‡πÅ‡∏•‡πâ‡∏ß break
(‡∏ô‡∏≠‡∏Å‡∏•‡∏π‡∏õ) ‡πÉ‡∏ä‡πâ if ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ò‡∏á found_admin ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏û‡∏¥‡∏°‡∏û‡πå‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏ó‡∏µ‡πà‡∏Ñ‡∏≤‡∏î‡∏´‡∏ß‡∏±‡∏á: "‡∏û‡∏ö Admin ‡πÉ‡∏ô‡∏£‡∏∞‡∏ö‡∏ö"'''
def check_admin(playesr:list):

    found_admin = False

    for e in playesr:
        if e == 'Admin':
            found_admin = True
            break
    
    if found_admin:
        print('‡∏û‡∏ö Admin ‡πÉ‡∏ô‡∏£‡∏∞‡∏ö‡∏ö')
    else:
        print('‡πÑ‡∏°‡πà‡∏û‡∏ö Admin ‡πÉ‡∏ô‡∏£‡∏∞‡∏ö‡∏ö')

players = ["Tom", "Spike", "Admin", "Jerry"]
