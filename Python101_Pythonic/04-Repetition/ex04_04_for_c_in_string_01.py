'''üìù 5 ‡πÇ‡∏à‡∏ó‡∏¢‡πå‡∏ù‡∏∂‡∏Å for c in string
1. üîç ‡πÇ‡∏à‡∏ó‡∏¢‡πå: ‡∏ô‡∏±‡∏ö‡∏≠‡∏±‡∏Å‡∏Ç‡∏£‡∏∞‡πÄ‡∏â‡∏û‡∏≤‡∏∞ (Case-Insensitive Count)
Input: sentence = "Success is not final, failure is not fatal."
‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡πÉ‡∏ä‡πâ‡∏ß‡∏á‡∏ß‡∏ô for ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ô‡∏±‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ‡∏ï‡∏±‡∏ß‡∏≠‡∏±‡∏Å‡∏©‡∏£ 's' ‡∏´‡∏£‡∏∑‡∏≠ 'S' ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏Å‡∏µ‡πà‡∏ï‡∏±‡∏ß‡πÉ‡∏ô‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ
‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏ó‡∏µ‡πà‡∏Ñ‡∏≤‡∏î‡∏´‡∏ß‡∏±‡∏á: 6 ‡∏ï‡∏±‡∏ß'''
def check_count_s():
    
    sentence = "Success is not final, failure is not fatal."
    count_s = 0

    new_sentence = sentence.lower()

    for c in new_sentence:
        if c == 's':
            count_s += 1
        
    print('Count s =', count_s)

check_count_s()

'''2. üî¢ ‡πÇ‡∏à‡∏ó‡∏¢‡πå: ‡∏Å‡∏£‡∏≠‡∏á‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç (Filter Digits)
Input: text_mixed = "Product Code: P78-02A (Price: 500)"

‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡πÉ‡∏ä‡πâ‡∏ß‡∏á‡∏ß‡∏ô for ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏î‡∏∂‡∏á‡πÄ‡∏â‡∏û‡∏≤‡∏∞ ‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç (Digits) ‡∏≠‡∏≠‡∏Å‡∏°‡∏≤‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ï‡πà‡∏≠‡∏Å‡∏±‡∏ô‡πÄ‡∏õ‡πá‡∏ô‡∏™‡∏ï‡∏£‡∏¥‡∏á‡πÉ‡∏´‡∏°‡πà

‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏ó‡∏µ‡πà‡∏Ñ‡∏≤‡∏î‡∏´‡∏ß‡∏±‡∏á: "7802500"

‡∏Ñ‡∏≥‡πÉ‡∏ö‡πâ: ‡πÉ‡∏ä‡πâ if char.isdigit(): ‡πÅ‡∏•‡πâ‡∏ß‡πÄ‡∏≠‡∏≤‡πÑ‡∏õ‡∏ï‡πà‡∏≠‡πÉ‡∏ô‡∏™‡∏ï‡∏£‡∏¥‡∏á‡∏ß‡πà‡∏≤‡∏á (new_string = new_string + char)'''
def check_digits():
    
    text_mixed = "Product Code: P78-02A (Price: 500)"
    digits = ''

    for c in text_mixed:
        if c.isdigit():
            digits += c

    print('digits =', digits)
check_digits()

'''3. ‚¨ú ‡πÇ‡∏à‡∏ó‡∏¢‡πå: ‡∏ô‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡πÄ‡∏ß‡πâ‡∏ô‡∏ß‡∏£‡∏£‡∏Ñ
Input: t = "I was an assistant prevention officer"
‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡πÉ‡∏ä‡πâ‡∏ß‡∏á‡∏ß‡∏ô for ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ô‡∏±‡∏ö‡∏ß‡πà‡∏≤‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ‡∏ô‡∏µ‡πâ‡∏°‡∏µ ‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á (Space) ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏Å‡∏µ‡πà‡∏ï‡∏±‡∏ß
‡∏Ñ‡∏≥‡πÉ‡∏ö‡πâ: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÄ‡∏á‡∏∑‡πà‡∏≠‡∏ô‡πÑ‡∏Ç if char == ' ': '''
def check_space():

    t = "I was an assistant prevention officer"
    space = 0
    
    for c in t:
        if c == ' ':
            space += 1
    
    print('Space = ', space)

check_space()

'''4. üî† ‡πÇ‡∏à‡∏ó‡∏¢‡πå: ‡∏™‡∏£‡πâ‡∏≤‡∏á Command/Slug (Transformation)
Input: title = "Database Migration Status"
‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ô‡∏µ‡πâ‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô Command Format ‡πÇ‡∏î‡∏¢ 
‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ó‡∏∏‡∏Å‡∏ï‡∏±‡∏ß‡∏≠‡∏±‡∏Å‡∏©‡∏£‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡∏û‡∏¥‡∏°‡∏û‡πå‡πÉ‡∏´‡∏ç‡πà ‡πÅ‡∏•‡∏∞ 
‡πÅ‡∏ó‡∏ô‡∏ó‡∏µ‡πà‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏î‡πâ‡∏ß‡∏¢‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏´‡∏°‡∏≤‡∏¢‡∏Ç‡∏µ‡∏î‡∏•‡πà‡∏≤‡∏á (_)
‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏ó‡∏µ‡πà‡∏Ñ‡∏≤‡∏î‡∏´‡∏ß‡∏±‡∏á: "DATABASE_MIGRATION_STATUS"
‡∏Ñ‡∏≥‡πÉ‡∏ö‡πâ: ‡∏ï‡πâ‡∏≠‡∏á‡πÉ‡∏ä‡πâ if-else (‡∏´‡∏£‡∏∑‡∏≠ if-elif) ‡∏†‡∏≤‡∏¢‡πÉ‡∏ô‡∏•‡∏π‡∏õ'''
def convert_str_to_upper():

    title = 'Database Migration Status'
    upper_title = title.upper()
    new_title = ''
    
    for char in upper_title:
        
        if char == ' ':
            new_title += '_'
        
        else:
            new_title += char 

    print('Title Upper _ =', new_title)

convert_str_to_upper()


'''5. ‚ùå ‡πÇ‡∏à‡∏ó‡∏¢‡πå: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡πÉ‡∏ô‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô (Boolean Flag)

Input: password = "user_01" (‡∏•‡∏≠‡∏á‡∏ó‡∏î‡∏™‡∏≠‡∏ö‡∏î‡πâ‡∏ß‡∏¢ "user name" ‡∏î‡πâ‡∏ß‡∏¢)
‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢: ‡∏à‡∏á‡πÉ‡∏ä‡πâ‡∏ß‡∏á‡∏ß‡∏ô for ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô ‡∏°‡∏µ‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
‡∏ñ‡πâ‡∏≤ ‡∏û‡∏ö ‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á ‡πÉ‡∏´‡πâ‡∏û‡∏¥‡∏°‡∏û‡πå "Invalid Password"
‡∏ñ‡πâ‡∏≤ ‡πÑ‡∏°‡πà‡∏û‡∏ö ‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡πÄ‡∏•‡∏¢ ‡πÉ‡∏´‡πâ‡∏û‡∏¥‡∏°‡∏û‡πå "Valid Password"

‡∏Ñ‡∏≥‡πÉ‡∏ö‡πâ: ‡∏õ‡πã‡∏≤‡∏Ñ‡∏ß‡∏£‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏±‡∏ß‡πÅ‡∏õ‡∏£ Boolean Flag (‡πÄ‡∏ä‡πà‡∏ô has_space = False) 
‡πÑ‡∏ß‡πâ‡∏ô‡∏≠‡∏Å‡∏•‡∏π‡∏õ ‡πÅ‡∏•‡πâ‡∏ß‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÄ‡∏õ‡πá‡∏ô True ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÄ‡∏à‡∏≠‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏Ñ‡∏£‡∏±‡∏ö 
(‡∏ñ‡πâ‡∏≤‡∏õ‡πã‡∏≤‡πÄ‡∏£‡∏µ‡∏¢‡∏ô‡∏£‡∏π‡πâ‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á break ‡πÅ‡∏•‡πâ‡∏ß ‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÉ‡∏ä‡πâ break ‡πÑ‡∏î‡πâ‡πÄ‡∏•‡∏¢)'''

def check_password_space(password:str):

    has_space = False

    for char in password:
        
        if char == ' ':
            has_space = True 
            break
    
    if has_space:
        print('Invalid Password')
    else:
        print('Valid Password')

