def longest_common_prefix(words):
    if not words:
        return ''
    
    base = words[0]
    prefix = ''

    for i in range(len(base)):

        ch = base[i]

        for j in range(1, len(words)):
            if i >= len(words[j]) or words[j][i] != ch:
                return prefix
        
        prefix += ch

