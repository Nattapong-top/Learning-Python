def column_sum(n_list:list):
    if not n_list:
       return []
    # 1 ‡∏´‡∏≤‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏°‡∏≤‡∏Å‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î
    max_cols = 0
    for row in n_list:
       if len(row) > max_cols:
          max_cols = len(row)
    # 2 ‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏° list ‡∏ú‡∏•‡∏£‡∏ß‡∏°‡∏Ç‡∏≠‡∏á‡πÅ‡∏ï‡πà‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå
    total_list = [0] * max_cols
    # 3 ‡πÑ‡∏•‡πà‡∏ó‡∏∏‡∏Å‡πÅ‡∏ñ‡∏ß ‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå
    for row in n_list:
      for i in range(len(row)):
          if isinstance(row[i], int):
              total_list[i] += row[i]
    return total_list

print(column_sum(n_list))

def column_sum_pythonic(n_list):
   max_cols = max((len(row) for row in n_list),default=0)

   return [
      sum(
         row[i]
         for row in n_list
         if i < len(row) and isinstance(row[i], int)
      )
      for i in range(max_cols)
   ]
