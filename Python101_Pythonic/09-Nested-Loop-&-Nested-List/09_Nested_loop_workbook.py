'''
| ‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î | `-` | `*` |
| ------ | --- | --- |
| 1      | 0   | 1   |
| 2      | 1   | 1   |
| 3      | 2   | 2   |
| 4      | 3   | 3   |
'''

def nested_loop_basic_1(n:int):
    for i in range(1, n+1):
        for _ in range(i-1):
            print('-', end='')
        
        start_count = 1 if i == 1 else i - 1
        for _ in range(start_count):
            print('*', end='')

        print()

