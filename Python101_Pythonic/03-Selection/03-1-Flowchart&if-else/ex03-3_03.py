    # print(check_ping)

    check_total = check_status or check_ping
    print('‡πÄ‡∏ô‡πá‡∏ï‡πÑ‡∏°‡πà‡πÄ‡∏™‡∏ñ‡∏µ‡∏¢‡∏£',check_total)

