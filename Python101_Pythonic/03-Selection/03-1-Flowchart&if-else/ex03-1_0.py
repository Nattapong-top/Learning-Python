def fizzbuzz(num: int) -> str:
    if num % 3 == 0 and num % 5 == 0:
        return f'{num} FizzBuzz'
    
    elif num % 3 == 0:
        return f'{num} Fizz'
    
    elif num % 5 == 0:
        return f'{num} Buzz'
    
    else:
        return num

